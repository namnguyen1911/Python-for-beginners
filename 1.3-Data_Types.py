#----------------------------------------

#Variables in Python can store different data type

#Text
a = "name"                  #str

#Numeric
b = 1                       #int
c = 2.2                     #float
d = 1j                      #complex

#Sequence
e = ["a", "b", "c"]         #list
f = (1, 2, 3)               #tuple
g = range(4)                #range

#Mapping
h = {"val": 1,"add": 22}    #dict

#Set
i = {"ab", "cd", "ef"}      #set
g = frozenset({0, 1, 2})    #frozenset

#Boolean
k = True                    #boolean

#Binary
l = b"Hello"                #bytes
m = bytearray(4)            #bytearray
n = memoryview(bytes(3))    #memoryview

#Print values
print (a)
print (b)
print (c)
print (d)
print (e)
print (f)
print (g)
print (h)
print (i)
print (g)
print (k)
print (l)
print (m)
print (n)

